export function formatBP(amount: number): string {
  if (amount >= 1000000000000) {
    return `${(amount / 1000000000000).toFixed(1)}T`;
  }
  if (amount >= 100000000) {
    return `${(amount / 100000000).toFixed(2)}B BP`;
  }
  if (amount >= 10000) {
    return `${(amount / 10000).toFixed(0)}만 BP`;
  }
  return `${amount} BP`;
}
